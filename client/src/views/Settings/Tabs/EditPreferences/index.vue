<template>
  <div class="row justify-around">
    <q-btn
      v-for="(preference, key) in getPreferences"
      :key="key"
      @click="updatePref({key: key, updates: {chosen: !preference.chosen}})"
      size="40px"
      round
      class="q-ma-xs"
    >
      <q-avatar size="120px">
        <img :src="preference.src" :class ="{chosen: preference.chosen}"/>
          <div :class="[preference.chosen ? 'chosen' : 'img-caption',
            'absolute-center',
            'text-subtitle1',
            'text-center']"
          >
          {{preference.hashtag.slice(1)}}
          </div>
      </q-avatar>
    </q-btn>
    <q-btn
      size="40px"
      round
      color="teal"
      @click="getTags(getPreferences)"

    > go
    </q-btn>
  </div>
</template>

<script>
import { mapGetters, mapActions } from 'vuex'
import axios from 'axios'

export default {
  name: 'EditPreferences',
  methods: {
    // ...mapActions('preferences', ['updatePreference', 'rollbackChanges']),
    ...mapActions({ updatePref: 'preferences/updatePreference', rollback: 'preferences/rollbackChanges' }),
    getTags: function (state) {
      let tags = []
      for (let obj in state) {
        if (state[obj].chosen) {
          tags.push(state[obj].hashtag)
        }
      }
      // console.log(this.$store)
      // this.rollback(tags)
      console.log(tags)
      axios.post('/api/preferences/save', tags)
        .then((response) => {
          console.log(response)
          alert('data saved')
        })
        .catch(error => {
          this.rollback(tags)
          if (error.response) {
            console.log(error.response.data)
            console.log(error.response.status)
            console.log(error.response.headers)
          } else if (error.request) {
            console.log(error.requset)
          } else {
            console.log('Error', error.message)
          }
          alert('Oooops, something went wrong!')
        })
    }
  },
  computed: {
    ...mapGetters('preferences', ['getPreferences'])
  }
}
</script>

